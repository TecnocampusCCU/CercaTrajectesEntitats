        global conn
        global Fitxer
        limitUsuari = self.dlg.SB_camins.value()
        if self.dlg.tabWidget_Destino.currentIndex() == 0:
            count = 'select count(*) from \"' + self.dlg.comboCapaDesti.currentText() + '\";'
